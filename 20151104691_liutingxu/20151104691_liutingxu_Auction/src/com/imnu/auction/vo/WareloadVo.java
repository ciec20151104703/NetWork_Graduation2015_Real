package com.imnu.auction.vo;

/**
 * 
 * 描述:钱包支出Vo类
 */
public class WareloadVo {
	private String wrId;

    private String waId;

    private String wrMoney;

    private String wrDate2;

    private String wrContent;

    
	public String getWrId() {
		return wrId;
	}

	public void setWrId(String wrId) {
		this.wrId = wrId;
	}

	public String getWaId() {
		return waId;
	}

	public void setWaId(String waId) {
		this.waId = waId;
	}

	public String getWrMoney() {
		return wrMoney;
	}

	public void setWrMoney(String wrMoney) {
		this.wrMoney = wrMoney;
	}
	
	public String getWrDate2() {
		return wrDate2;
	}

	public void setWrDate2(String wrDate2) {
		this.wrDate2 = wrDate2;
	}

	public String getWrContent() {
		return wrContent;
	}

	public void setWrContent(String wrContent) {
		this.wrContent = wrContent;
	}
}
