public class JsonMsg {
	private int datas;	//状态
	private String msg;	//描述信息
	public int getDatas() {
		return datas;
	}
	public void setDatas(int datas) {
		this.datas = datas;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	
	
}
